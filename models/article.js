const mongoose = require('mongoose');

const ArticleSchema = new mongoose.Schema({
  keyword: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  text: {
    type: String,
    required: true,
  },
  date: {
    type: String,
    required: true,
  },
  source: {
    type: String,
    required: true,
  },
  link: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  owner: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'user',
    required: true,
    autopopulate: true,
    select: false,
  },
}, {
  versionKey: false,
});
ArticleSchema.plugin(require('mongoose-autopopulate'));

module.exports = mongoose.model('article', ArticleSchema);
